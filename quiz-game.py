def playQuiz(questions):
    score = 0
    for i in range(10):
        print(f"\nQuestion {i+1}/10: {questions[i]['prompt']}")
        for j in range(4):
            print(questions[i]['options'][j])
        ans = input("Your answer (A, B, C, or D): ").upper()
        
        if ans == questions[i]["answer"]:
            print("✅ Correct Bingo!")
            score += 1
        else:
            print(f"❌ Nope! The correct answer was {questions[i]['answer']}")
    
    print(f"\nYour final score: {score}/10")
    
    if score == 10:
        print("🏆 PERFECT SCORE! You're a champion!")
    elif score >= 8:
        print("👍 Great job!")
    elif score >= 6:
        print("👌 Good effort!")
    else:
        print("💪 Keep practicing!")

